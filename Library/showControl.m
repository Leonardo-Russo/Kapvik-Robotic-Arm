function showControl(tc, theta, thetad, thetadd, qDes,qdDes,...
                  qddDes, E, tau, tauMotor, iMotor, typeOfControl, tauMotorMax, iMotorMax)

if typeOfControl=="Sto2Nav"
    figure('name', 'Control Stowage to Navigation (Angles, velocities, acceleration)')
elseif typeOfControl=="Sto2Ret"
    figure('name', 'Control Stowage to Retrieval (Angles, velocities, acceleration)')
elseif typeOfControl=="Ret2Trans"
    figure('name', 'Control Retrieval to Transfer (Angles, velocities, acceleration)')
else
    error('Specify type of controller')
end
subplot(3,4,1)
plot(tc,rad2deg(theta(1,:)),'b',lineWidth=1.5)
hold on
plot(tc,rad2deg(qDes(1,:))+3,'--r',lineWidth=1.5)
plot(tc,rad2deg(qDes(1,:))-3,'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$q_1(t)$$ $$[^\circ]$$','Interpreter','latex')
title('Joint angles 1','Interpreter','latex')
set(gca,'FontSize',15)
subplot(3,4,2)
plot(tc,rad2deg(theta(2,:)),'b',lineWidth=1.5)
hold on
plot(tc,rad2deg(qDes(2,:))+3,'--r',lineWidth=1.5)
plot(tc,rad2deg(qDes(2,:))-3,'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$q_2(t)$$ $$[^\circ]$$','Interpreter','latex')
title('Joint angles 2','Interpreter','latex')
set(gca,'FontSize',15)
subplot(3,4,3)
plot(tc,rad2deg(theta(3,:)),'b',lineWidth=1.5)
hold on
plot(tc,rad2deg(qDes(3,:))+3,'--r',lineWidth=1.5)
plot(tc,rad2deg(qDes(3,:))-3,'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$q_3(t)$$ $$[^\circ]$$','Interpreter','latex')
title('Joint angles 3','Interpreter','latex')
set(gca,'FontSize',15)
subplot(3,4,4)
plot(tc,rad2deg(theta(4,:)),'b',lineWidth=1.5)
hold on
plot(tc,rad2deg(qDes(4,:))+3,'--r',lineWidth=1.5)
plot(tc,rad2deg(qDes(4,:))-3,'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$q_4(t)$$ $$[^\circ]$$','Interpreter','latex')
title('Joint angles 4','Interpreter','latex')
set(gca,'FontSize',15)

subplot(3,4,5)
plot(tc,rad2deg(thetad(1,:)),'b',lineWidth=1.5)
hold on
plot(tc,rad2deg(qdDes(1,:))+1,'--r',lineWidth=1.5)
plot(tc,rad2deg(qdDes(1,:))-1,'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$\dot q_1(t)$$ $$[^\circ/s]$$','Interpreter','latex')
title('Joint velocities 1','Interpreter','latex')
set(gca,'FontSize',15)
subplot(3,4,6)
plot(tc,rad2deg(thetad(2,:)),'b',lineWidth=1.5)
hold on
plot(tc,rad2deg(qdDes(2,:))+1,'--r',lineWidth=1.5)
plot(tc,rad2deg(qdDes(2,:))-1,'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$\dot q_2(t)$$ $$[^\circ/s]$$','Interpreter','latex')
title('Joint velocities 2','Interpreter','latex')
set(gca,'FontSize',15)
subplot(3,4,7)
plot(tc,rad2deg(thetad(3,:)),'b',lineWidth=1.5)
hold on
plot(tc,rad2deg(qdDes(3,:))+1,'--r',lineWidth=1.5)
plot(tc,rad2deg(qdDes(3,:))-1,'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$\dot q_3(t)$$ $$[^\circ/s]$$','Interpreter','latex')
title('Joint velocities 3','Interpreter','latex')
set(gca,'FontSize',15)
subplot(3,4,8)
plot(tc,rad2deg(thetad(4,:)),'b',lineWidth=1.5)
hold on
plot(tc,rad2deg(qdDes(4,:))+1,'--r',lineWidth=1.5)
plot(tc,rad2deg(qdDes(4,:))-1,'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$\dot q_4(t)$$ $$[^\circ/s]$$','Interpreter','latex')
title('Joint velocities 4','Interpreter','latex')
set(gca,'FontSize',15)

subplot(3,4,9)
plot(tc,rad2deg(thetadd(1,:)),'b',lineWidth=1.5)
hold on
plot(tc,rad2deg(qddDes(1,:))+3,'--r',lineWidth=1.5)
plot(tc,rad2deg(qddDes(1,:))-3,'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$\ddot q_1(t)$$ $$[^\circ/s^2]$$','Interpreter','latex')
title('Joint acceleration 1','Interpreter','latex')
set(gca,'FontSize',15)
subplot(3,4,10)
plot(tc,rad2deg(thetadd(2,:)),'b',lineWidth=1.5)
hold on
plot(tc,rad2deg(qddDes(2,:))+3,'--r',lineWidth=1.5)
plot(tc,rad2deg(qddDes(2,:))-3,'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$\ddot q_2(t)$$ $$[^\circ/s^2]$$','Interpreter','latex')
title('Joint acceleration 2','Interpreter','latex')
set(gca,'FontSize',15)
subplot(3,4,11)
plot(tc,rad2deg(thetadd(3,:)),'b',lineWidth=1.5)
hold on
plot(tc,rad2deg(qddDes(3,:))+3,'--r',lineWidth=1.5)
plot(tc,rad2deg(qddDes(3,:))-3,'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$\ddot q_3(t)$$ $$[^\circ/s^2]$$','Interpreter','latex')
title('Joint acceleration 3','Interpreter','latex')
set(gca,'FontSize',15)
subplot(3,4,12)
plot(tc,rad2deg(thetadd(4,:)),'b',lineWidth=1.5)
hold on
plot(tc,rad2deg(qddDes(4,:))+3,'--r',lineWidth=1.5)
plot(tc,rad2deg(qddDes(4,:))-3,'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$\ddot q_4(t)$$ $$[^\circ/s^2]$$','Interpreter','latex')
title('Joint acceleration 4','Interpreter','latex')
set(gca,'FontSize',15)

if typeOfControl=="Sto2Nav"
    figure('name', 'Control Stowage to Navigation (Error and Control Torque load side)')
elseif typeOfControl=="Sto2Ret"
    figure('name', 'Control Stowage to Retrieval (Error and Control Torque load side)')
elseif typeOfControl=="Ret2Trans"
    figure('name', 'Control Retrieval to Transfer (Error and Control Torque load side)')
else
    error('Specify type of controller')
end

subplot(2,2,1)
plot(tc,rad2deg(E(1,:)),'b',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$\Delta e_1=q_{1,des}-q_1$$ $$[^\circ]$$','Interpreter','latex')
title('Error angle Joint 1','Interpreter','latex')
set(gca,'FontSize',15)
subplot(2,2,2)
plot(tc,rad2deg(E(2,:)),'b',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$\Delta e_2=q_{2,des}-q_2$$ $$[^\circ]$$','Interpreter','latex')
title('Error angle Joint 2','Interpreter','latex')
set(gca,'FontSize',15)
subplot(2,2,3)
plot(tc,rad2deg(E(3,:)),'b',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$\Delta e_3=q_{3,des}-q_3$$ $$[^\circ]$$','Interpreter','latex')
title('Error angle Joint 3','Interpreter','latex')
set(gca,'FontSize',15)
subplot(2,2,4)
plot(tc,rad2deg(E(4,:)),'b',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$\Delta e_4=q_{4,des}-q_4$$ $$[^\circ]$$','Interpreter','latex')
title('Error angle Joint 4','Interpreter','latex')
set(gca,'FontSize',15)

if typeOfControl=="Sto2Nav"
    figure('name', 'Control Stowage to Navigation (Control Torque and Control Current motor side)')
elseif typeOfControl=="Sto2Ret"
    figure('name', 'Control Stowage to Retrieval (Control Torque and Control Current motor side)')
elseif typeOfControl=="Ret2Trans"
    figure('name', 'Control Retrieval to Transfer (Control Torque and Control Current motor side)')
else
    error('Specify type of controller')
end
subplot(2,4,1)
plot(tc,tauMotor(1,:)*10^(3),'b',lineWidth=1.5)
hold on
plot(tc,(tauMotorMax*10^(3))*ones(1,length(tc)),'--r',lineWidth=1.5)
plot(tc,-(tauMotorMax*10^(3))*ones(1,length(tc)),'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$\tau_{1,m}(t)$$ [mNm]','Interpreter','latex')
title('Control torque Joint 1','Interpreter','latex')
set(gca,'FontSize',15)
subplot(2,4,2)
plot(tc,tauMotor(2,:)*10^(3),'b',lineWidth=1.5)
hold on
plot(tc,(tauMotorMax*10^(3))*ones(1,length(tc)),'--r',lineWidth=1.5)
plot(tc,-(tauMotorMax*10^(3))*ones(1,length(tc)),'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$\tau_{2,m}(t)$$ [mNm]','Interpreter','latex')
title('Control torque Joint 2','Interpreter','latex')
set(gca,'FontSize',15)
subplot(2,4,3)
plot(tc,tauMotor(3,:)*10^(3),'b',lineWidth=1.5)
hold on
plot(tc,(tauMotorMax*10^(3))*ones(1,length(tc)),'--r',lineWidth=1.5)
plot(tc,-(tauMotorMax*10^(3))*ones(1,length(tc)),'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$\tau_{3,m}(t)$$ [mNm]','Interpreter','latex')
title('Control torque Joint 3','Interpreter','latex')
set(gca,'FontSize',15)
subplot(2,4,4)
plot(tc,tauMotor(4,:)*10^(3),'b',lineWidth=1.5)
hold on
plot(tc,(tauMotorMax*10^(3))*ones(1,length(tc)),'--r',lineWidth=1.5)
plot(tc,-(tauMotorMax*10^(3))*ones(1,length(tc)),'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$\tau_{4,m}(t)$$ [mNm]','Interpreter','latex')
title('Control torque Joint 4','Interpreter','latex')
set(gca,'FontSize',15)

subplot(2,4,5)
plot(tc,iMotor(1,:),'b',lineWidth=1.5)
hold on
plot(tc,iMotorMax*ones(1,length(tc)),'--r',lineWidth=1.5)
plot(tc,-iMotorMax*ones(1,length(tc)),'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$i_{1,m}(t)$$ [A]','Interpreter','latex')
title('Control current Joint 1','Interpreter','latex')
set(gca,'FontSize',15)
subplot(2,4,6)
plot(tc,iMotor(2,:),'b',lineWidth=1.5)
hold on
plot(tc,iMotorMax*ones(1,length(tc)),'--r',lineWidth=1.5)
plot(tc,-iMotorMax*ones(1,length(tc)),'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$i_{2,m}(t)$$ [A]','Interpreter','latex')
title('Control current Joint 2','Interpreter','latex')
set(gca,'FontSize',15)
subplot(2,4,7)
plot(tc,iMotor(3,:),'b',lineWidth=1.5)
hold on
plot(tc,iMotorMax*ones(1,length(tc)),'--r',lineWidth=1.5)
plot(tc,-iMotorMax*ones(1,length(tc)),'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$i_{3,m}(t)$$ [A]','Interpreter','latex')
title('Control current Joint 3','Interpreter','latex')
set(gca,'FontSize',15)
subplot(2,4,8)
plot(tc,iMotor(4,:),'b',lineWidth=1.5)
hold on
plot(tc,iMotorMax*ones(1,length(tc)),'--r',lineWidth=1.5)
plot(tc,-iMotorMax*ones(1,length(tc)),'--r',lineWidth=1.5)
xlabel('$$t$$ [s]','Interpreter','latex')
ylabel('$$i_{4,m}(t)$$ [A]','Interpreter','latex')
title('Control current Joint 4','Interpreter','latex')
set(gca,'FontSize',15)

end