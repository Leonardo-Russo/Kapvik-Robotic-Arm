function slider = createSlider(name, minVal, maxVal, initVal, callback, position)
    slider = uicontrol('Style', 'slider', ...
                       'Min', minVal, 'Max', maxVal, ...
                       'Value', initVal, ...
                       'Position', position, ...
                       'Callback', callback, ...
                       'Tag', name);  % Tag used to identify the slider
end